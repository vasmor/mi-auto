/**
 * Hook into admin_init to check for the import GET parameter.
 */
function miauto_import_model_hero_all_init() {
	if ( ! is_admin() || ! current_user_can( 'manage_options' ) ) {
		return;
	}

	if ( empty( $_GET['miauto_import_model_hero_all'] ) || '1' !== $_GET['miauto_import_model_hero_all'] ) {
		return;
	}

	if ( ! isset( $_GET['_wpnonce'] ) || ! wp_verify_nonce( sanitize_text_field( wp_unslash( $_GET['_wpnonce'] ) ), 'miauto_import_model_hero_all' ) ) {
		add_action( 'admin_notices', function () {
			$url = wp_nonce_url( admin_url( '?miauto_import_model_hero_all=1' ), 'miauto_import_model_hero_all' );
			echo '<div class="notice notice-info"><p>MI-AUTO: Для импорта hero-контента всех моделей перейдите по ссылке: <a href="' . esc_url( $url ) . '">Импортировать hero всех моделей</a></p></div>';
		} );
		return;
	}

	set_time_limit( 300 );
	wp_raise_memory_limit( 'admin' );

	$result = miauto_run_model_hero_all_import();

	if ( is_wp_error( $result ) ) {
		$msg = $result->get_error_message();
		add_action( 'admin_notices', function () use ( $msg ) {
			echo '<div class="notice notice-error"><p>MI-AUTO: ' . esc_html( $msg ) . '</p></div>';
		} );
		return;
	}

	$log = $result;
	add_action( 'admin_notices', function () use ( $log ) {
		echo '<div class="notice notice-success"><p>MI-AUTO: Импорт hero всех моделей завершён.</p><pre style="background:#f9f9f9;padding:10px;max-height:600px;overflow:auto">' . esc_html( $log ) . '</pre></div>';
	} );
}
add_action( 'admin_init', 'miauto_import_model_hero_all_init' );

/**
 * Main import runner.
 *
 * @return string|WP_Error  Log on success, WP_Error on failure.
 */
function miauto_run_model_hero_all_import() {
	if ( ! function_exists( 'carbon_set_post_meta' ) ) {
		return new WP_Error( 'cf_missing', 'Carbon Fields API не доступна.' );
	}

	$all_data = miauto_model_hero_get_all_data();
	$log      = "=== Импорт hero + SEO для всех моделей ===\n\n";

	$stats = array(
		array( 'md_stat_value' => '5,0',    'md_stat_label' => 'Рейтинг на картах' ),
		array( 'md_stat_value' => '500+',   'md_stat_label' => 'Отзывов на картах' ),
		array( 'md_stat_value' => 'с 2005', 'md_stat_label' => 'Опыт работы' ),
	);

	foreach ( $all_data as $slug => $data ) {
		$log .= "--- {$slug} ---\n";

		$posts = get_posts( array(
			'post_type'      => 'miauto_model',
			'name'           => $slug,
			'posts_per_page' => 1,
			'post_status'    => 'any',
		) );

		if ( empty( $posts ) ) {
			$log .= "  SKIP: запись не найдена\n\n";
			continue;
		}

		$post_id = $posts[0]->ID;
		$title   = $posts[0]->post_title;
		$log    .= "  Post ID: {$post_id} ({$title})\n";

		// Subtitle.
		carbon_set_post_meta( $post_id, 'miauto_md_hero_subtitle', $data['subtitle'] );
		$log .= "  OK: subtitle\n";

		// Features.
		carbon_set_post_meta( $post_id, 'miauto_md_hero_features', $data['features'] );
		$log .= "  OK: features (" . count( $data['features'] ) . " пунктов)\n";

		// CTA.
		carbon_set_post_meta( $post_id, 'miauto_md_hero_cta_primary_text', 'Записаться на диагностику' );
		carbon_set_post_meta( $post_id, 'miauto_md_hero_cta_secondary_text', 'Рассчитать стоимость' );
		$log .= "  OK: CTA кнопки\n";

		// Hero image.
		$thumb_id = get_post_thumbnail_id( $post_id );
		if ( $thumb_id ) {
			carbon_set_post_meta( $post_id, 'miauto_md_hero_image', $thumb_id );
			$log .= "  OK: hero_image = #{$thumb_id}\n";
		} else {
			$log .= "  SKIP: hero_image — нет миниатюры\n";
		}

		// Stats.
		carbon_set_post_meta( $post_id, 'miauto_md_hero_stats', $stats );
		$log .= "  OK: stats\n";

		// SEO post_content.
		wp_update_post( array(
			'ID'           => $post_id,
			'post_content' => $data['seo_text'],
		) );
		$log .= "  OK: post_content (" . mb_strlen( $data['seo_text'] ) . " символов)\n\n";
	}

	$log .= "=== Импорт завершён ===\n";
	return $log;
}

